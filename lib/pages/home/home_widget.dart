    logFirebaseEvent('screen_view', parameters: {'screen_name': 'home'});
    // On page load action.
    SchedulerBinding.instance.addPostFrameCallback((_) async {
      _model.apiResult4n2 = await ProdStatusGlobalCall.call(
        userToken: valueOrDefault<String>(
          FFAppState().token,
          '0',
        ),
      );
      if ((_model.apiResult4n2?.succeeded ?? true)) {
        setState(() {
          FFAppState().userClockIn = ProdStatusGlobalCall.isClockedIn(
            (_model.apiResult4n2?.jsonBody ?? ''),
          );
        });
        if (ProdStatusGlobalCall.isClockedIn(
          (_model.apiResult4n2?.jsonBody ?? ''),
        )) {
          setState(() {
            FFAppState().userWfh = ProdStatusGlobalCall.isWfh(
              (_model.apiResult4n2?.jsonBody ?? ''),
            );
          });
        } else {
          setState(() {});
        }
      } else {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(
              ProdStatusGlobalCall.message(
                (_model.apiResult4n2?.jsonBody ?? ''),
              ).toString(),
              style: TextStyle(
                color: FlutterFlowTheme.of(context).secondaryBackground,
              ),
            ),
            duration: Duration(milliseconds: 4000),
            backgroundColor: FlutterFlowTheme.of(context).secondary,
          ),
        );

        context.pushNamed('login_page');

        return;
      }
    });

    setupAnimations(
      animationsMap.values.where((anim) =>
          anim.trigger == AnimationTrigger.onActionTrigger ||
          !anim.applyInitialState),
      this,
    );
  }

